let currentLang = localStorage.getItem('love_hunt_lang') || 'en';

/**
 * Returns current language code ('en' or 'ar')
 */
export function getLang() {
  return currentLang;
}

/**
 * Toggles the language between English and Arabic, updating UI.
 * @param {string} lang 'en' or 'ar'
 */
export function setLang(lang) {
  if (lang !== 'en' && lang !== 'ar') return;
  currentLang = lang;
  localStorage.setItem('love_hunt_lang', lang);
  
  // Set HTML document direction
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  
  // Apply translation to all data-i18n elements
  translatePage();
}

/**
 * Scans the DOM and translates all elements carrying data-i18n attributes.
 */
export function translatePage() {
  const dict = TRANSLATIONS[currentLang];
  
  document.querySelectorAll('[data-i18n]').forEach(elem => {
    const key = elem.getAttribute('data-i18n');
    if (dict[key]) {
      // Check if it's an input or textarea to set placeholder
      if (elem.tagName === 'INPUT' || elem.tagName === 'TEXTAREA') {
        elem.placeholder = dict[key];
      } else {
        // Keep icons inside button or text intact if they exist
        const icon = elem.querySelector('i');
        if (icon) {
          elem.textContent = " " + dict[key] + " ";
          // Re-insert icon at proper side
          if (currentLang === 'ar') {
            elem.appendChild(icon); // Put icon at left/right
          } else {
            elem.insertBefore(icon, elem.firstChild);
          }
        } else {
          elem.textContent = dict[key];
        }
      }
    }
  });

  // Specially translate toggling labels or text direction rules in CSS
  document.body.className = currentLang === 'ar' ? 'rtl-mode' : 'ltr-mode';
}
